			<th>How To Use Binary Newsgroups</th>
		</tr>
		<tr>
			<td>
				<div class="pageTitle"><span class="pageTitle">With Xnews, for Windows</span><br/>
				<span class="subTitle">By <a href="mailto:craig@patik.com">Craig</a></span></div>

				<br/>

				<p>Download <a href="http://xnews.newsguy.com/">Xnews</a>. There are many newsreaders that support binaries, but Xnews is free and easy to use. Unzip Xnews.exe to a folder on your hard drive.</p>
				<p>Find out your news server address. If you signed up for a pay server, you can find the server name on their website. To use your ISP's server, check <a href="http://www.newsbin.com/ISP.htm">this list</a>.</p>

				<p>Double-click on the <tt>xnews.exe</tt> file. First it will ask you to enter your news server's address and give it a nickname or alias.</p>

				<div class="bin"><img src="binaries/binaries_xnews_01.png" alt="Enter your news server address" title="Enter your news server address" /></div>

				<div class="bin"><img src="binaries/binaries_xnews_02.png" alt="Enter your news server alias" title="Enter your news server alias" /></div>

				<p>Next you will be presented with the Setup dialog. Enter your username and password if applicable (note: if you're using your ISP's server, you probably don't need to enter anything). Click OK.</p>

				<div class="bin"><img src="binaries/binaries_xnews_03.png" alt="Enter your username and password" title="Enter your username and password" /></div>

				<p>Now it will ask you to download the entire list of newsgroups. Click OK.</p>

				<div class="bin"><img src="binaries/binaries_xnews_04.png" alt="Click OK to download list" title="Click OK to download list" /></div>

				<p>Once the list loads, you can narrow it down by typing the name of the desired newsgroup into the filter field. For this example we will download files from <tt>alt.binaries.music.shn.dmb</tt>. When you see your group in the list, click the button next to its name to subscribe to the group.</p>

				<div class="bin"><img src="binaries/binaries_xnews_05.png" alt="Subscribe to the group" title="Subscribe to the group" /></div>

				<p>Double-click on the group name to open the group and begin downloading messages. If there are more than 50,000 new messagesin the group Xnews will prompt you to continue; just click OK.</p>

				<div class="bin"><img src="binaries/binaries_xnews_06.png" alt="Combine the parts" title="Combine the parts" /></div>

				<p>When it finishes, right-click on the <tt>T</tt> button and then press <tt>T</tt> on your keyboard to combine the threads. Binary messages are posted to Usenet in parts, and Xnews conveniently groups all of the parts together.</p>

				<div class="bin"><img src="binaries/binaries_xnews_07.png" alt="Icon types" title="Icon types" /></div>

				<p>You'll notice a few different icons next to the messages. A notepad icon denotes a text message; these can be viewed simply by double-clicking on the subject of the message. The light blue icons denote a complete binary file; these must be decoded before viewing. A darker blue icon means parts are missing for this file. You cannot download an incomplete file, so you will need to either wait for the uploader to finish posting the parts or request a repost. Occasionally parts get lost during transfer and causes an incomplete file.</p>

				<p>Find the files you want to download and select them. The easiest way is to select the first file, hold down the <tt>shift</tt> key on your keyboard, then click on the last file. Alternatively you can hold down the <tt>control</tt> key while clicking on individual files to select them.</p>

				<p>Once you have selected the files, click the <tt>Articles</tt> menu, then <tt>Queue</tt>, or just hit the <tt>spacebar</tt>.</p>

				<div class="bin"><img src="binaries/binaries_xnews_08.png" alt="Select the files and queue" title="Select the files and queue" /></div>

				<p>Now you've queued those files that you selected. Optionally you can select more files and queue them as well. Note the number in the <tt>Q</tt> column; this denotes the order which they will be downloaded. You can rearrange the order if you wish by queuing/dequeuing particular files.</p>

				<div class="bin"><img src="binaries/binaries_xnews_09.png" alt="Decode the queued files" title="Decode the queued files" /></div>

				<p>Once you've queued the files, click on the Decode icon or press <tt>F4</tt> and specify a directory to download to.</p>

				<div class="bin"><img src="binaries/binaries_xnews_10.png" alt="Select a directory" title="Select a directory" /></div>

				<p>Notes:</p>
					<ul>
						<li><tt>F5</tt> checks for more messages</li>
						<li>Files will expire, so download older posts first. Typical retention is anywhere from 2 days for ISP servers, or 14-21 days for pay servers.</li>
						<li>Sometimes the poster will include <tt>.PAR</tt> files, which help fix files that are incomplete on your server. To help with these files, use <a href="http://www.smr-usenet.com/tutor/smartpar.shtml">SmartPar</a>.</li>
					</ul>

